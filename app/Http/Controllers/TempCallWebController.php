    public function nextQueue()
    {
        $data = null;
        $queue = TempCallWeb::listNewest()->take(1)->get()->first();
        if ($queue) {
            if ($queue->Tampil == 'n') {
                $data = $queue;
                $queue->update(['Tampil' => 'y']);
            }
        }

        return response()->json([
            'queue' => $data,
        ], 200);
    }

